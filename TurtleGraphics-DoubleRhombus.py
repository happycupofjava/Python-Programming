vru = turtle.Turtle()
vru.hideturtle()
vru.setheading(135)
for x in range(4):
    vru.forward(100)
    vru.right(90)
vru.right(90)
vru.forward(100)
for x in range(4):
    vru.forward(100)
    vru.right(90)
turtle.done()

